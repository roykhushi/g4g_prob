class Solution {
  public:
    void sort012(vector<int>& arr) {
        // code here
        //dutch national flag algo
        int low =0;
        int mid = 0;
        int high = arr.size()-1;
        
        while(mid>=low && mid<=high){
            if(arr[mid] == 0){
                int temp = arr[low];
                arr[low] = arr[mid];
                arr[mid] = temp;
                low++;
                mid++;
            }
            else if(arr[mid] == 2){
                int res = arr[high];
                arr[high] = arr[mid];
                arr[mid] = res;
                // mid++;
                high--;
            }
            else{
                mid++;
            }
        }
        
    }
};


